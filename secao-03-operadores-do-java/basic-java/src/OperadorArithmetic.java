public class OperadorArithmetic {

    public static void main(String[] args) {

        int a = 10;
        int b = 2;
        int c = 5;

        int d = a * b - c;
        System.out.println(d);
        int e = 4;
        int f = 2;

        int g = e * (f / 2);
        System.out.println(g);
    }
}
